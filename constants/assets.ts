const enharmonicNotes = {
    'B#': 'C',
    'E#': 'F',
    'C#': 'Db',
    'D#': 'Eb',
    'F#': 'Gb',
    'G#': 'Ab',
    'A#': 'Bb',
}

export function getNoteMp3(note: string) {
    const noteName = note.replace(/\d+/g, '');
    if(noteName in enharmonicNotes) {
        const enharmonicNoteName = enharmonicNotes[noteName as keyof typeof enharmonicNotes];
        const noteOctave = note.replace(/[^\d]/g, '');
        return noteMp3s[enharmonicNoteName + noteOctave as keyof typeof noteMp3s];
    }
    return noteMp3s[note as keyof typeof noteMp3s];
}

type Clef = 'bass' | 'treble' | 'none';
type Note = keyof typeof noteSvgs | keyof typeof trebleNoteSvgs;

export const getNoteSvg = (note: Note, clef: Clef = 'bass') => {
    if (clef === 'treble') {
        return trebleNoteSvgs[note as keyof typeof trebleNoteSvgs];
    }
    return noteSvgs[note as keyof typeof noteSvgs];
};

export const getNotePng = (note: Note, clef: Clef = 'bass') => {
    const modified_note = note.replace("#", "").replace("b", "")
    if (clef === 'treble') {
        return trebleNotePngs[modified_note as keyof typeof trebleNotePngs];
    }
    return notePngs[modified_note as keyof typeof notePngs];
}

